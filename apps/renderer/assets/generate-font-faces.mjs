// One-off/regeneratable script: downloads the `latin` subset of each curated
// caption font (all Google Fonts, OFL-licensed) and emits a self-contained
// @font-face stylesheet with base64-embedded woff2 data. Run with:
//   node apps/renderer/assets/generate-font-faces.mjs
//
// Data URIs are protocol-agnostic (unlike file:// or bare paths), so the
// generated stylesheet works identically in the Tauri preview webview and in
// the headless Chrome instance render-cli drives for export — no loopback
// media server needed for fonts the way ADR-0011 needed one for video.

import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_CSS = path.join(HERE, 'caption-fonts.generated.css');

const FAMILIES = 'family=Poppins:wght@400;600;700&family=Montserrat:wght@400;700&family=Oswald:wght@400;700&family=Anton&family=Bebas+Neue&family=Playfair+Display:wght@400;700&family=Caveat:wght@400;700&family=Permanent+Marker';

async function main() {
  const cssUrl = `https://fonts.googleapis.com/css2?${FAMILIES}&display=swap`;
  const res = await fetch(cssUrl, {
    headers: {
      // Google Fonts serves woff2 only to modern user agents.
      'User-Agent':
        'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
    },
  });
  if (!res.ok) throw new Error(`Failed to fetch font manifest: ${res.status}`);
  const css = await res.text();

  // One @font-face block per (family, weight, subset). Keep only `latin`
  // (and unmarked, single-subset families like Permanent Marker) to keep the
  // bundle lean; non-Latin scripts fall back to the OS per source: 'system'
  // font options already in the picker.
  const blockPattern = /(?:\/\*\s*(\w[\w-]*)\s*\*\/\s*)?@font-face\s*{([^}]*)}/g;
  const faces = [];
  let match;
  while ((match = blockPattern.exec(css))) {
    const [, subset, body] = match;
    if (subset && subset !== 'latin') continue;
    const family = /font-family:\s*'([^']+)'/.exec(body)?.[1];
    const weight = /font-weight:\s*(\d+)/.exec(body)?.[1];
    const url = /url\(([^)]+)\)/.exec(body)?.[1];
    if (!family || !weight || !url) continue;
    faces.push({ family, weight, url });
  }

  const seen = new Set();
  const unique = faces.filter((face) => {
    const key = `${face.family}:${face.weight}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });

  const rules = [];
  for (const face of unique) {
    process.stdout.write(`Downloading ${face.family} ${face.weight}...\n`);
    const fontRes = await fetch(face.url);
    if (!fontRes.ok) throw new Error(`Failed to download ${face.url}: ${fontRes.status}`);
    const buffer = Buffer.from(await fontRes.arrayBuffer());
    const base64 = buffer.toString('base64');
    rules.push(
      `@font-face {\n  font-family: '${face.family}';\n  font-style: normal;\n  font-weight: ${face.weight};\n  font-display: swap;\n  src: url(data:font/woff2;base64,${base64}) format('woff2');\n}`,
    );
  }

  const header = `/**\n * Curated caption font pack. Auto-generated by generate-font-faces.mjs.\n * Data-URI embedded: zero network calls at app runtime, zero dependency on\n * how the host serves static assets (Tauri webview, headless Chrome export).\n * Fonts: Poppins, Montserrat, Oswald, Anton, Bebas Neue, Playfair Display,\n * Caveat, Permanent Marker — all Google Fonts, SIL Open Font License 1.1.\n */\n\n`;
  await writeFile(OUTPUT_CSS, header + rules.join('\n\n') + '\n');
  process.stdout.write(`Wrote ${unique.length} font faces to ${OUTPUT_CSS}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
